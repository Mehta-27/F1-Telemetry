def get_fastest_lap_telemetry(raw_laps_df, driver_code):
    """
    Extracts the high-resolution 10Hz car telemetry (Speed, Throttle, Brake) 
    for a specific driver's fastest lap.
    Note: We must pass the RAW fastf1 laps object here, not the cleaned pandas dataframe.
    """
    print(f"System: Extracting high-res telemetry for {driver_code}...")
    
    try:
        # 1. Isolate the driver's fastest lap
        fastest_lap = raw_laps_df.pick_drivers(driver_code).pick_fastest()
        
        # 2. Extract the car's telemetry for that specific lap
        telemetry = fastest_lap.get_telemetry()
        
        # 3. Add the driver code so we know whose data this is when we mix them later
        telemetry['Driver'] = driver_code
        
        # 4. Keep only the ML-relevant columns (dropping things like session time)
        features = ['Driver', 'Distance', 'Speed', 'Throttle', 'Brake', 'nGear', 'RPM' , 'X' , 'Y']
        clean_telemetry = telemetry[features]
        
        print(f"Success: Extracted {len(clean_telemetry)} data points for {driver_code}.")
        return clean_telemetry
        
    except Exception as e:
        print(f"Error extracting telemetry for {driver_code}: {e}")
        return None


def save_to_feature_store(df, folder_name, file_name):
    """
    Saves DataFrames as highly compressed Parquet files.
    This acts as our Data Lake / Feature Store.
    """
    # Create the directory if it doesn't exist
    store_path = f'./feature_store/{folder_name}'
    os.makedirs(store_path, exist_ok=True)
    
    # Save as Parquet
    full_path = f'{store_path}/{file_name}.parquet'
    df.to_parquet(full_path, index=False)
    print(f"System: Saved data to {full_path}")