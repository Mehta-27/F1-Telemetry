import pandas as pd
import os

def clean_race_laps(laps_df):
    """
    Takes raw fastf1 laps data and transforms it into a clean, 
    ML-ready feature set of pure racing pace.
    """
    print("System: Starting data cleaning...")
    
    # 1. The 'IsAccurate' filter: fastf1's built-in flag that removes 
    # Safety Cars, VSCs, and extreme anomalies.
    clean_df = laps_df[laps_df['IsAccurate'] == True].copy()
    
    # 2. Drop Pit Laps: Remove any lap where the car entered or exited the pits.
    clean_df = clean_df[(clean_df['PitOutTime'].isnull()) & (clean_df['PitInTime'].isnull())]
    
    # 3. Format Conversion: ML models need numbers, not timestamps. 
    # Convert LapTime to total seconds (e.g., 1:15.500 becomes 75.500)
    clean_df['LapTime_Seconds'] = clean_df['LapTime'].dt.total_seconds()
    
    # 4. Feature Selection: Keep only the columns that hold predictive value.
    features = [
        'Driver', 'LapNumber', 'LapTime_Seconds', 
        'Compound', 'TyreLife', 'Stint'
    ]
    clean_df = clean_df[features]
    
    # 5. Drop any lingering NaNs (missing data) in our target variable
    clean_df = clean_df.dropna(subset=['LapTime_Seconds'])
    
    print(f"Success: Filtered down to {len(clean_df)} pure racing laps.")
    return clean_df

